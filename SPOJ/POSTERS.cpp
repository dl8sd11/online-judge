int main()
{
    IOS();
    cin >> t;
    while (t--) {
        event.clear();
        pst.clear();
        cin >> n;
        REP1 (i, n) {
            int l, r;
            cin >> l >> r;
            event.eb(l, -i);
            event.eb(r, i);
        }
        memset(app, 0, (n+1) * sizeof(bool));

        sort(ALL(event));
        REP (i, SZ(event)) {
            int hd = i;
            while (i < SZ(event) && event[i].X == event[hd].X && event[i].Y < 0) {
                pst.insert(-event[i].Y);
                i++;
            }

            app[*prev(pst.end())] = true;

            while (i < SZ(event) && event[i].X == event[hd].X) {
                assert(event[i].Y > 0);
                pst.erase(event[i].Y);
                i++;
            }
            i--;
        }

        int ans = 0;
        REP1 (i, n) {
            ans += app[i];
        }

        cout << ans << endl;
    }
    return 0;
}
